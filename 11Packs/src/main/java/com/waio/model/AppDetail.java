package com.waio.model;

public class AppDetail {

	private String latestAppName;
	private String latestAppVersion;
	private String latestAppUpdate;
	/**
	 * @return the latestAppName
	 */
	public String getLatestAppName() {
		return latestAppName;
	}
	/**
	 * @param latestAppName the latestAppName to set
	 */
	public void setLatestAppName(String latestAppName) {
		this.latestAppName = latestAppName;
	}
	/**
	 * @return the latestAppVersion
	 */
	public String getLatestAppVersion() {
		return latestAppVersion;
	}
	/**
	 * @param latestAppVersion the latestAppVersion to set
	 */
	public void setLatestAppVersion(String latestAppVersion) {
		this.latestAppVersion = latestAppVersion;
	}
	/**
	 * @return the latestAppUpdate
	 */
	public String getLatestAppUpdate() {
		return latestAppUpdate;
	}
	/**
	 * @param latestAppUpdate the latestAppUpdate to set
	 */
	public void setLatestAppUpdate(String latestAppUpdate) {
		this.latestAppUpdate = latestAppUpdate;
	}
}
